package entity;

public class WealthDetail {

    private String name;
    private String officeLocId;
    private String officeTsKey;
    private String type;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOfficeLocId() {
        return officeLocId;
    }

    public void setOfficeLocId(String officeLocId) {
        this.officeLocId = officeLocId;
    }

    public String getOfficeTsKey() {
        return officeTsKey;
    }

    public void setOfficeTsKey(String officeTsKey) {
        this.officeTsKey = officeTsKey;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}