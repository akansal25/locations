package entity;

public class Holidays {
    private String closedAllDay;

    private String name;

    public String getClosedAllDay() {
        return closedAllDay;
    }

    public void setClosedAllDay(String closedAllDay) {
        this.closedAllDay = closedAllDay;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
